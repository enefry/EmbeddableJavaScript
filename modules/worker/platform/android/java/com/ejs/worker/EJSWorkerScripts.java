        builder.append("                }\n");
        builder.append("                return new ctor(bytes, byteOffset, node.elements);\n");
        builder.append("            }\n");
        builder.append("            if (node.kind === \"array\") {\n");
        builder.append("                const values = Array.isArray(node.value) ? node.value : [];\n");
        builder.append("                const out = new Array(values.length);\n");
        builder.append("                for (let i = 0; i < values.length; i++) {\n");
        builder.append("                    out[i] = decodeValue(values[i]);\n");
        builder.append("                }\n");
        builder.append("                return out;\n");
        builder.append("            }\n");
        builder.append("            if (node.kind === \"object\") {\n");
        builder.append("                const out = {};\n");
