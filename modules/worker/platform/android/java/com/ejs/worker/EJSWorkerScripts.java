        builder.append("\n");
        builder.append("            if (codePoint <= 0x7f) {\n");
        builder.append("                bytes.push(codePoint);\n");
        builder.append("            } else if (codePoint <= 0x7ff) {\n");
        builder.append("                bytes.push(0xc0 | (codePoint >> 6));\n");
        builder.append("                bytes.push(0x80 | (codePoint & 0x3f));\n");
        builder.append("            } else if (codePoint <= 0xffff) {\n");
        builder.append("                bytes.push(0xe0 | (codePoint >> 12));\n");
        builder.append("                bytes.push(0x80 | ((codePoint >> 6) & 0x3f));\n");
        builder.append("                bytes.push(0x80 | (codePoint & 0x3f));\n");
        builder.append("            } else {\n");
        builder.append("                bytes.push(0xf0 | (codePoint >> 18));\n");
        builder.append("                bytes.push(0x80 | ((codePoint >> 12) & 0x3f));\n");
        builder.append("                bytes.push(0x80 | ((codePoint >> 6) & 0x3f));\n");
        builder.append("                bytes.push(0x80 | (codePoint & 0x3f));\n");
        builder.append("            }\n");
        builder.append("        }\n");
        builder.append("        return new Uint8Array(bytes);\n");
        builder.append("    }\n");
        builder.append("\n");
        builder.append("    function decodeUtf8(input) {\n");
        builder.append("        const bytes = asUint8Array(input);\n");
        builder.append("        if (typeof TextDecoder !== \"undefined\") {\n");
        builder.append("            return new TextDecoder(\"utf-8\").decode(bytes);\n");
        builder.append("        }\n");
        builder.append("        let output = \"\";\n");
        builder.append("        function isContinuation(byte) {\n");
        builder.append("            return byte >= 0x80 && byte <= 0xbf;\n");
        builder.append("        }\n");
        builder.append("        for (let i = 0; i < bytes.length;) {\n");
        builder.append("            const first = bytes[i++];\n");
        builder.append("            let codePoint = first;\n");
        builder.append("            if (first <= 0x7f) {\n");
        builder.append("                codePoint = first;\n");
        builder.append("            } else if (first >= 0xc2 && first <= 0xdf) {\n");
        builder.append("                if (i < bytes.length && isContinuation(bytes[i])) {\n");
        builder.append("                    const second = bytes[i++];\n");
        builder.append("                    codePoint = ((first & 0x1f) << 6) | (second & 0x3f);\n");
        builder.append("                } else {\n");
        builder.append("                    codePoint = 0xfffd;\n");
        builder.append("                }\n");
        builder.append("            } else if (first >= 0xe0 && first <= 0xef) {\n");
        builder.append("                if (i + 1 < bytes.length && isContinuation(bytes[i]) && isContinuation(bytes[i + 1])) {\n");
        builder.append("                    const second = bytes[i++];\n");
        builder.append("                    const third = bytes[i++];\n");
        builder.append("                    codePoint = ((first & 0x0f) << 12) | ((second & 0x3f) << 6) | (third & 0x3f);\n");
        builder.append("                } else {\n");
        builder.append("                    codePoint = 0xfffd;\n");
        builder.append("                }\n");
        builder.append("            } else if (first >= 0xf0 && first <= 0xf4) {\n");
        builder.append("                if (i + 2 < bytes.length && isContinuation(bytes[i]) && isContinuation(bytes[i + 1]) && isContinuation(bytes[i + 2])) {\n");
        builder.append("                    const second = bytes[i++];\n");
        builder.append("                    const third = bytes[i++];\n");
        builder.append("                    const fourth = bytes[i++];\n");
        builder.append("                    codePoint = ((first & 0x07) << 18) | ((second & 0x3f) << 12) | ((third & 0x3f) << 6) | (fourth & 0x3f);\n");
        builder.append("                } else {\n");
        builder.append("                    codePoint = 0xfffd;\n");
        builder.append("                }\n");
        builder.append("            } else {\n");
        builder.append("                codePoint = 0xfffd;\n");
        builder.append("            }\n");
        builder.append("            if (codePoint <= 0xffff) {\n");
        builder.append("                output += String.fromCharCode(codePoint);\n");
        builder.append("            } else {\n");
        builder.append("                codePoint -= 0x10000;\n");
        builder.append("                output += String.fromCharCode(0xd800 + (codePoint >> 10));\n");
        builder.append("                output += String.fromCharCode(0xdc00 + (codePoint & 0x3ff));\n");
        builder.append("            }\n");
        builder.append("        }\n");
        builder.append("        return output;\n");
        builder.append("    }\n");
        builder.append("\n");
        builder.append("    function decodeFrame(frameData) {\n");
        builder.append("        const bytes = asUint8Array(frameData);\n");
        builder.append("        if (bytes.length < 4) {\n");
        builder.append("            throw new Error(\"Worker frame is truncated\");\n");
        builder.append("        }\n");
        builder.append("        const headerLength = bytes[0] | (bytes[1] << 8) | (bytes[2] << 16) | (bytes[3] << 24);\n");
        builder.append("        const headerEnd = 4 + headerLength;\n");
        builder.append("        if (headerLength < 0 || headerEnd > bytes.length) {\n");
        builder.append("            throw new Error(\"Worker frame header is invalid\");\n");
        builder.append("        }\n");
        builder.append("        const header = JSON.parse(decodeUtf8(bytes.subarray(4, headerEnd)));\n");
        builder.append("        const payload = bytes.subarray(headerEnd).slice().buffer;\n");
        builder.append("        return {\n");
        builder.append("            envelope: header.envelope || {},\n");
        builder.append("            sidecar: payload\n");
        builder.append("        };\n");
        builder.append("    }\n");
        builder.append("\n");
        builder.append("    function isPlainObject(value) {\n");
        builder.append("        if (value == null || typeof value !== \"object\") {\n");
        builder.append("            return false;\n");
        builder.append("        }\n");
        builder.append("        const proto = Object.getPrototypeOf(value);\n");
        builder.append("        return proto === Object.prototype || proto === null;\n");
        builder.append("    }\n");
        builder.append("\n");
        builder.append("    function normalizeTransferList(transferList) {\n");
        builder.append("        if (transferList == null) {\n");
        builder.append("            return [];\n");
        builder.append("        }\n");
        builder.append("        if (typeof transferList[Symbol.iterator] !== \"function\") {\n");
        builder.append("            throw new TypeError(\"transferList must be iterable\");\n");
        builder.append("        }\n");
        builder.append("        const seen = new Set();\n");
        builder.append("        const result = [];\n");
        builder.append("        for (const entry of transferList) {\n");
        builder.append("            let buffer = null;\n");
        builder.append("            if (entry instanceof ArrayBuffer) {\n");
        builder.append("                buffer = entry;\n");
        builder.append("            } else if (ArrayBuffer.isView(entry)) {\n");
