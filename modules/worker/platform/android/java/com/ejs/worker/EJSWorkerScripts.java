        builder.append("                return out;\n");
        builder.append("            }\n");
        builder.append("            throw new TypeError(\"Unsupported worker payload kind\");\n");
        builder.append("        }\n");
        builder.append("\n");
        builder.append("        return decodeValue(envelope.payload);\n");
        builder.append("    }\n");
