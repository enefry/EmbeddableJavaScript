        builder.append("    function serializeForMessage(value, transferList) {\n");
        builder.append("        const transferBuffers = normalizeTransferList(transferList);\n");
        builder.append("        const transferSet = new Set(transferBuffers);\n");
        builder.append("        const seenObjects = new Set();\n");
        builder.append("        const encounteredBuffers = new Set();\n");
        builder.append("        const bufferRecords = [];\n");
        builder.append("        const bufferIDs = new Map();\n");
        builder.append("        let totalLength = 0;\n");
        builder.append("\n");
        builder.append("        function appendBuffer(buffer) {\n");
        builder.append("            if (bufferIDs.has(buffer)) {\n");
        builder.append("                return bufferRecords[bufferIDs.get(buffer)];\n");
        builder.append("            }\n");
        builder.append("            const copy = new Uint8Array(buffer).slice();\n");
        builder.append("            const entry = {\n");
        builder.append("                id: bufferRecords.length,\n");
        builder.append("                bytes: copy,\n");
        builder.append("                offset: totalLength,\n");
        builder.append("                length: copy.length,\n");
        builder.append("                transfer: transferSet.has(buffer)\n");
        builder.append("            };\n");
        builder.append("            bufferIDs.set(buffer, entry.id);\n");
        builder.append("            bufferRecords.push(entry);\n");
        builder.append("            totalLength += copy.length;\n");
        builder.append("            return entry;\n");
        builder.append("        }\n");
        builder.append("\n");
        builder.append("        function encodeValue(innerValue) {\n");
        builder.append("            if (innerValue === null) return { kind: \"null\" };\n");
        builder.append("            const valueType = typeof innerValue;\n");
        builder.append("            if (valueType === \"boolean\" || valueType === \"string\") {\n");
        builder.append("                return { kind: valueType, value: innerValue };\n");
        builder.append("            }\n");
        builder.append("            if (valueType === \"number\") {\n");
        builder.append("                if (!Number.isFinite(innerValue)) {\n");
        builder.append("                    throw new TypeError(\"Only finite numbers are supported in worker messages\");\n");
        builder.append("                }\n");
        builder.append("                return { kind: \"number\", value: innerValue };\n");
        builder.append("            }\n");
        builder.append("            if (valueType === \"undefined\") {\n");
        builder.append("                return { kind: \"undefined\" };\n");
        builder.append("            }\n");
        builder.append("            if (valueType === \"function\" || valueType === \"symbol\" || valueType === \"bigint\") {\n");
        builder.append("                throw new TypeError(\"Unsupported value in worker message\");\n");
        builder.append("            }\n");
        builder.append("            if (hasSharedArrayBuffer(innerValue)) {\n");
        builder.append("                throw new TypeError(\"SharedArrayBuffer is not supported\");\n");
        builder.append("            }\n");
        builder.append("\n");
        builder.append("            if (innerValue instanceof ArrayBuffer) {\n");
        builder.append("                encounteredBuffers.add(innerValue);\n");
        builder.append("                const record = appendBuffer(innerValue);\n");
        builder.append("                return {\n");
        builder.append("                    kind: \"arraybuffer\",\n");
        builder.append("                    buffer: record.id\n");
        builder.append("                };\n");
        builder.append("            }\n");
        builder.append("\n");
        builder.append("            if (ArrayBuffer.isView(innerValue)) {\n");
        builder.append("                if (hasSharedArrayBuffer(innerValue.buffer)) {\n");
        builder.append("                    throw new TypeError(\"SharedArrayBuffer is not supported\");\n");
        builder.append("                }\n");
        builder.append("                encounteredBuffers.add(innerValue.buffer);\n");
        builder.append("                const record = appendBuffer(innerValue.buffer);\n");
        builder.append("                return {\n");
        builder.append("                    kind: \"view\",\n");
        builder.append("                    ctor: innerValue.constructor ? innerValue.constructor.name : \"\",\n");
        builder.append("                    buffer: record.id,\n");
        builder.append("                    byteOffset: innerValue.byteOffset,\n");
        builder.append("                    byteLength: innerValue.byteLength,\n");
        builder.append("                    elements: typeof innerValue.length === \"number\" ? innerValue.length : null,\n");
        builder.append("                    transfer: transferSet.has(innerValue.buffer)\n");
        builder.append("                };\n");
        builder.append("            }\n");
        builder.append("\n");
        builder.append("            if (innerValue instanceof Date ||\n");
        builder.append("                innerValue instanceof Map ||\n");
        builder.append("                innerValue instanceof Set ||\n");
        builder.append("                innerValue instanceof RegExp ||\n");
        builder.append("                innerValue instanceof Error ||\n");
        builder.append("                innerValue instanceof WeakMap ||\n");
        builder.append("                innerValue instanceof WeakSet) {\n");
        builder.append("                throw new TypeError(\"Unsupported object type in worker message\");\n");
        builder.append("            }\n");
        builder.append("\n");
        builder.append("            if (seenObjects.has(innerValue)) {\n");
        builder.append("                throw new TypeError(\"Cyclic structures are not supported in worker messages\");\n");
        builder.append("            }\n");
        builder.append("            seenObjects.add(innerValue);\n");
        builder.append("            try {\n");
        builder.append("                if (Array.isArray(innerValue)) {\n");
        builder.append("                    return {\n");
        builder.append("                        kind: \"array\",\n");
        builder.append("                        value: innerValue.map(encodeValue)\n");
        builder.append("                    };\n");
        builder.append("                }\n");
        builder.append("                if (!isPlainObject(innerValue)) {\n");
        builder.append("                    throw new TypeError(\"Unsupported object type in worker message\");\n");
        builder.append("                }\n");
        builder.append("                const entries = [];\n");
        builder.append("                const keys = Object.keys(innerValue);\n");
        builder.append("                for (let i = 0; i < keys.length; i++) {\n");
        builder.append("                    const key = keys[i];\n");
        builder.append("                    entries.push([key, encodeValue(innerValue[key])]);\n");
        builder.append("                }\n");
        builder.append("                return {\n");
        builder.append("                    kind: \"object\",\n");
        builder.append("                    value: entries\n");
        builder.append("                };\n");
        builder.append("            } finally {\n");
        builder.append("                seenObjects.delete(innerValue);\n");
        builder.append("            }\n");
        builder.append("        }\n");
        builder.append("\n");
        builder.append("        const payload = encodeValue(value);\n");
        builder.append("        for (let i = 0; i < transferBuffers.length; i++) {\n");
        builder.append("            if (!encounteredBuffers.has(transferBuffers[i])) {\n");
        builder.append("                throw new TypeError(\"transferList contains a buffer not present in the message\");\n");
        builder.append("            }\n");
        builder.append("        }\n");
        builder.append("\n");
        builder.append("        const sidecar = new Uint8Array(totalLength);\n");
        builder.append("        const buffers = [];\n");
        builder.append("        for (let i = 0; i < bufferRecords.length; i++) {\n");
        builder.append("            const entry = bufferRecords[i];\n");
        builder.append("            sidecar.set(entry.bytes, entry.offset);\n");
        builder.append("            buffers.push({\n");
        builder.append("                offset: entry.offset,\n");
        builder.append("                length: entry.length,\n");
        builder.append("                transfer: entry.transfer\n");
        builder.append("            });\n");
        builder.append("        }\n");
        builder.append("\n");
        builder.append("        return {\n");
        builder.append("            envelope: {\n");
        builder.append("                kind: \"message\",\n");
        builder.append("                version: 1,\n");
        builder.append("                payload,\n");
        builder.append("                buffers\n");
        builder.append("            },\n");
        builder.append("            sidecar: sidecar.buffer,\n");
        builder.append("            detachers: transferBuffers\n");
        builder.append("        };\n");
        builder.append("    }\n");
