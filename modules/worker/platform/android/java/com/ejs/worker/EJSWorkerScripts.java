        builder.append("        }\n");
        builder.append("\n");
        builder.append("        function decodeValue(node) {\n");
        builder.append("            if (!node || typeof node !== \"object\") {\n");
        builder.append("                throw new TypeError(\"Invalid worker payload node\");\n");
        builder.append("            }\n");
        builder.append("            if (node.kind === \"null\") return null;\n");
        builder.append("            if (node.kind === \"undefined\") return undefined;\n");
        builder.append("            if (node.kind === \"boolean\" || node.kind === \"string\" || node.kind === \"number\") return node.value;\n");
        builder.append("            if (node.kind === \"arraybuffer\") {\n");
        builder.append("                if (Number.isInteger(node.buffer)) {\n");
        builder.append("                    return decodeBuffer(node.buffer);\n");
        builder.append("                }\n");
        builder.append("                return sliceBuffer(node.offset, node.length);\n");
        builder.append("            }\n");
        builder.append("            if (node.kind === \"view\") {\n");
        builder.append("                let bytes;\n");
        builder.append("                let byteOffset = 0;\n");
        builder.append("                let byteLength = node.length;\n");
